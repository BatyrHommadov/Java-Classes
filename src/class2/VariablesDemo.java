package class2;

public class VariablesDemo {
    public static void main(String[] args) {
        String name="Sam";
        int age=22; // we use int for age because it is the most common datatype for storing the whole numbers
        String city="Reading, Berkshire";
        int salary=120000;
        char gender='M';
        String number="+168-4984-4848";
        boolean smart=false;

        System.out.println(name);
        System.out.println(age);
        System.out.println(city);
        System.out.println(salary);
        System.out.println(gender);
        System.out.println(number);
        System.out.println(smart);

    }
}
