package class6;

public class ClassTask2Increment {
    public static void main(String[] args){

        int num1=1;
        while(num1<101){
            System.out.println(num1);
            num1++;
        }
        int num2=0;
        while(num2<16){
            System.out.println(num2);
            num2+=1;
        }
        int num3=0;
        while(num3<51){
            System.out.println(num3);
            num3+=5;
        }
    }

}
