package class7;

public class ForLoops3 {
    public static void main(String[] args){

        /*
        When you know exactly how many times you want to loop through a block,
        use the ForLoop instead of WhileLoop;
        And when we don't know how many times we need to repeat a block of code we
        should be going with WhileLoop. Most of the time it happens when the user input is involved.
         */

        for (int i = 0; i < 10; i++) {
            System.out.println(i);

        }

        /*
        line 13---> initialisation
        i<10;
        sout(i);
        i++
        i<10;
        sout(i);
         */
    }
}
